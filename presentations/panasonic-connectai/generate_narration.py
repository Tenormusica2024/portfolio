output_dir = os.path.join(os.path.dirname(os.path.abspath(__file__)), "audio")
os.makedirs(output_dir, exist_ok=True)

for slide_num, text in narrations.items():
    padded = f"{slide_num:02d}"
    output_path = os.path.join(output_dir, f"slide_{padded}.mp3")
    
    if os.path.exists(output_path):
        print(f"[SKIP] slide_{padded}.mp3 already exists")
        continue
    
    tts = gTTS(text=text, lang='ja', slow=False)
    tts.save(output_path)
    size = os.path.getsize(output_path)
    print(f"[OK] slide_{padded}.mp3 ({size} bytes)")

print(f"\nDone! Generated {len(narrations)} narration files.")
